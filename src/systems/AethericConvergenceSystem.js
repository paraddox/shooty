    getActiveSystems() {
        const active = [];
        const now = Date.now();
        
        this.systemRegistry.forEach((sys, id) => {
            const instance = sys.getInstance();
            if (!instance) return;
            
            // Check if system is "active" based on its state
            const isActive = this.isSystemActive(instance, id);
            
            if (isActive) {
                if (!sys.lastActive) {
                    sys.activationTime = now;
                }
                active.push(sys);
            }
            
            sys.lastActive = isActive;
        });
        
        return active;
    }
    
    isSystemActive(instance, systemId) {
        // Check various activity indicators based on system type
        if (!instance) return false;
        
        // Generic checks
        if (instance.active === false) return false;
        if (instance.isActive === false) return false;
        if (instance.enabled === false) return false;
        
        // System-specific checks
        switch (systemId) {
            case 'echoStorm':
                return instance.isAbsorbing || instance.echoCount > 0;
            case 'fracture':
                return instance.inFractureMode || instance.fractureActive;
            case 'temporalResidue':
                return instance.residueNodes?.length > 0;
            case 'resonanceCascade':
                return instance.cascadeActive || instance.chainDepth > 0;
            case 'temporalSingularity':
                return instance.singularityActive || instance.hasSingularity;
            case 'omniWeapon':
                return instance.currentForm !== 'default' || instance.evolutionProgress > 0;
            case 'paradoxEngine':
                return instance.isPredicting || instance.predictionLines?.length > 0;
            case 'chronoLoop':
                return instance.isRecording || instance.hasRecording || instance.isRewinding;
            case 'quantumImmortality':
                return instance.isQuantum || instance.immortalityCharges > 0;
            case 'observerEffect':
                return instance.awareness > 0 || instance.isObserving;
            case 'voidCoherence':
                return instance.voidPower > 0 || instance.nodes?.length > 0;
            case 'tesseractTitan':
                return instance.titan?.active === true;
            case 'temporalContract':
                return instance.activeContracts?.length > 0;
            case 'causalEntanglement':
                return instance.entanglementMap?.size > 0;
            case 'symbioticPrediction':
                return instance.predictionConfidence > 0.5;
            case 'dimensionalCollapse':
                return instance.collapseActive || instance.collapseProgress > 0;
            case 'temporalRewind':
                return instance.isRewinding || instance.canRewind;
            case 'mnemosyneWeave':
                return instance.inTrance || instance.activeIncursion;
            case 'kairosMoment':
                return instance.inKairos || instance.flowState === 'perfect';
            case 'syntropyEngine':
                return instance.syntropy > 0;
            case 'nemesisGenesis':
                return instance.nemesis?.active === true;
            case 'oracleProtocol':
                return instance.oracleActive || instance.visions?.length > 0;
            case 'resonantWhispers':
                return instance.whispers?.length > 0;
            case 'egregoreProtocol':
                return instance.egregoreStage > 1;
            case 'timelineChronicle':
                return instance.isRecording;
            case 'cinematicArchive':
                return instance.isCapturing;
            default:
                return false;
        }
    }
    
