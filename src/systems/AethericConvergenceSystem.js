    saveToLexicon(convergence) {
        const existing = this.lexicon[convergence.id];
        
        if (!existing) {
            // New entry
            this.lexicon[convergence.id] = {
                name: convergence.name,
                discoverer: convergence.isFirstDiscovery ? 'You' : (convergence.discoverer || 'Unknown'),
                systems: convergence.systems,
                firstSeen: Date.now(),
                invocationCount: 1,
                lastInvocation: Date.now(),
                entropy: convergence.entropy,
                tier: convergence.tier,
                sigilHash: convergence.sigilHash,
                modifiers: convergence.modifiers
            };
        } else {
            // Update existing
            existing.invocationCount++;
            existing.lastInvocation = Date.now();
            existing.entropy = Math.max(existing.entropy, convergence.entropy);
        }
        
        this.saveLexicon();
    }
    
