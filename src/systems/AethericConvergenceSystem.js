    generateSigilHash(comboKey) {
        // Simple hash for visual consistency
        let hash = 0;
        for (let i = 0; i < comboKey.length; i++) {
            const char = comboKey.charCodeAt(i);
            hash = ((hash << 5) - hash) + char;
            hash = hash & hash;
        }
        return Math.abs(hash).toString(16).padStart(8, '0');
    }
    
