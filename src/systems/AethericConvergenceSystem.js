    generateProceduralSigil(convergence) {
        const container = this.scene.add.container(0, 0);
        
        // Get colors from involved systems
        const colors = convergence.systems.map(id => this.systemColors[id] || 0xffffff);
        const blendedColor = this.blendColors(colors);
        
        // Generate geometric shape based on number of systems
        const graphics = this.scene.add.graphics();
        graphics.lineStyle(3, blendedColor, 0.9);
        
        const radius = 40;
        const sides = Math.max(3, convergence.systems.length);
        
        // Draw polygon
        graphics.beginPath();
        for (let i = 0; i <= sides; i++) {
            const angle = (i / sides) * Math.PI * 2 - Math.PI / 2;
            const x = Math.cos(angle) * radius;
            const y = Math.sin(angle) * radius;
            if (i === 0) graphics.moveTo(x, y);
            else graphics.lineTo(x, y);
        }
        graphics.closePath();
        graphics.strokePath();
        
        // Draw inner pattern
        graphics.lineStyle(2, blendedColor, 0.6);
        const innerRadius = radius * 0.6;
        graphics.beginPath();
        for (let i = 0; i < sides; i++) {
            const angle = (i / sides) * Math.PI * 2 - Math.PI / 2;
            const x = Math.cos(angle) * innerRadius;
            const y = Math.sin(angle) * innerRadius;
            graphics.moveTo(0, 0);
            graphics.lineTo(x, y);
        }
        graphics.strokePath();
        
        // Add glow effect
        const glow = this.scene.add.graphics();
        glow.fillStyle(blendedColor, 0.2);
        glow.fillCircle(0, 0, radius + 10);
        container.add(glow);
        container.add(graphics);
        
        // Add pulsing animation
        this.scene.tweens.add({
            targets: glow,
            scale: { from: 1, to: 1.2 },
            alpha: { from: 0.2, to: 0.1 },
            duration: 1500,
            yoyo: true,
            repeat: -1,
            ease: 'Sine.inOut'
        });
        
        container.setDepth(1000);
        return container;
    }
    
    blendColors(colors) {
        if (colors.length === 0) return 0xffffff;
        if (colors.length === 1) return colors[0];
        
        let r = 0, g = 0, b = 0;
        colors.forEach(color => {
            r += (color >> 16) & 0xff;
            g += (color >> 8) & 0xff;
            b += color & 0xff;
        });
        
        r = Math.round(r / colors.length);
        g = Math.round(g / colors.length);
        b = Math.round(b / colors.length);
        
        return (r << 16) | (g << 8) | b;
    }
    
    updateConvergenceVisuals(time, delta) {
        // Update position of sigils to follow player
        const player = this.scene.player;
        if (!player) return;
        
        this.activeConvergences.forEach(convergence => {
            if (convergence.sigil) {
                convergence.sigil.x = player.x;
                convergence.sigil.y = player.y - 80;
                
                // Rotate based on intensity
                convergence.sigil.rotation += 0.01 * (1 + convergence.visualIntensity);
                
                // Update visual intensity
                convergence.visualIntensity = Math.min(
                    convergence.visualIntensity + 0.01,
                    1.0
                );
            }
            
            // Random particle emission
            if (Math.random() < 0.1 * convergence.visualIntensity) {
                const offset = 60;
                const x = player.x + (Math.random() - 0.5) * offset * 2;
                const y = player.y + (Math.random() - 0.5) * offset * 2;
                this.convergenceParticles.emitParticleAt(x, y);
            }
        });
    }
    
    updateFadingConvergences(delta) {
        const now = Date.now();
        
        for (let i = this.fadingConvergences.length - 1; i >= 0; i--) {
            const convergence = this.fadingConvergences[i];
            const fadeProgress = (now - convergence.fadeStartTime) / 2000; // 2 second fade
            
            if (fadeProgress >= 1) {
                // Fully faded, destroy
                if (convergence.sigil) {
                    convergence.sigil.destroy();
                }
                this.fadingConvergences.splice(i, 1);
            } else {
                // Update fade
                if (convergence.sigil) {
                    convergence.sigil.alpha = 1 - fadeProgress;
                    convergence.sigil.scale = 1 - fadeProgress * 0.5;
                }
            }
        }
    }
    
    updateModifiers(delta) {
        // Update modifier intensities
        const now = Date.now();
        
        this.activeModifiers.forEach((data, mod) => {
            // Intensity decays over time
            const age = now - data.startTime;
            data.intensity = Math.max(0.5, 1 - (age / 30000)); // 30 second decay
        });
    }
    
