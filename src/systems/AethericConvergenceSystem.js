    forceConvergence(systemIds) {
        // Manual synthesis attempt
        const systems = systemIds.map(id => this.systemRegistry.get(id)).filter(Boolean);
        
        if (systems.length < this.MIN_SYSTEMS_FOR_CONVERGENCE) {
            return { success: false, reason: 'Need at least 3 systems' };
        }
        
        // Activate systems if not already active
        // (This would need cooperation from each system)
        
        // Force convergence with bonus entropy
        const comboKey = systems.map(s => s.id).sort().join(',');
        const convergence = {
            id: comboKey,
            systems: systems.map(s => s.id),
            tier: 'major',
            entropy: 0.9,
            peakEntropy: 0.9,
            startTime: Date.now(),
            duration: 0,
            name: this.generateProceduralName(systems),
            sigilHash: this.generateSigilHash(comboKey),
            modifiers: this.generateModifiers(systems, 'major'),
            isFirstDiscovery: !this.lexicon[comboKey],
            visualIntensity: 0,
            isSynthesis: true
        };
        
        this.activateConvergence(convergence);
        this.saveToLexicon(convergence);
        
        return { success: true, convergence };
    }
    
