    generateProceduralName(systems) {
        // Generate a name based on the systems involved
        const systemNames = systems.map(s => {
            const name = s.id.replace(/([A-Z])/g, ' $1').toLowerCase();
            return name.split(' ').pop(); // Get last word
        });
        
        // Name templates
        const templates = [
            `${systemNames[0]} ${systemNames[1]}`,
            `${systemNames[0]} ${this.getActionWord()}`,
            `The ${this.getAdjective()} ${systemNames[0]}`,
            `${systemNames[0]} ${systemNames[1]} Convergence`,
            `${this.getAdjective()} ${systemNames[0]}`,
            `${systemNames[0]} of ${systemNames[1]}`,
            `Convergence of ${systemNames[0]} and ${systemNames[1]}`
        ];
        
        // Capitalize first letter of each word
        const name = templates[Math.floor(Math.random() * templates.length)];
        return name.replace(/\b\w/g, l => l.toUpperCase());
    }
    
    getActionWord() {
        const actions = ['Surge', 'Cascade', 'Echo', 'Bloom', 'Fracture', 
                        'Convergence', 'Nexus', 'Singularity', 'Resonance', 'Storm'];
        return actions[Math.floor(Math.random() * actions.length)];
    }
    
    getAdjective() {
        const adjectives = ['Temporal', 'Eternal', 'Shimmering', 'Crystalline', 
                           'Aetheric', 'Prismatic', 'Resonant', 'Living', 'Flowing'];
        return adjectives[Math.floor(Math.random() * adjectives.length)];
    }
    
    generateModifiers(systems, tier) {
        const modifiers = [];
        const modifierCount = tier === 'transcendent' ? 3 : tier === 'major' ? 2 : 1;
        
        // Collect available modifier genes from all systems
        const availableGenes = [];
        systems.forEach(sys => {
            const genes = this.modifierGenes[sys.id] || [];
            availableGenes.push(...genes);
        });
        
        // Randomly select modifiers
        for (let i = 0; i < modifierCount && availableGenes.length > 0; i++) {
            const index = Math.floor(Math.random() * availableGenes.length);
            modifiers.push(availableGenes[index]);
            availableGenes.splice(index, 1); // Remove to prevent duplicates
        }
        
        return modifiers;
    }
    
    applyModifiers(modifiers) {
        modifiers.forEach(mod => {
            this.activeModifiers.set(mod, {
                startTime: Date.now(),
                intensity: 1.0
            });
            
            // Apply the actual effect
            this.applyModifierEffect(mod);
        });
    }
    
    removeModifiers(modifiers) {
        modifiers.forEach(mod => {
            this.activeModifiers.delete(mod);
            this.removeModifierEffect(mod);
        });
    }
    
